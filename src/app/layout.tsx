export default function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {

  const [user, setUser] = useState<any>()
  useEffect(() => {
    async function getUser() {
      const {
        data: { user },
      } = await supabase.auth.getUser();
      if (user) {
        setUser(user);
      } else {
        setUser(null);
      }
    }
    getUser();
  }, []);
  return (
    <html lang="en">
      <body className="bg-white">
        <NextUIProvider>
          {/* <GlobalProvider> */}
            <Nav authUser={user} setAuthUser={setUser} />
            {children}
          {/* </GlobalProvider> */}
        </NextUIProvider>
      </body>
    </html>
  );
}
